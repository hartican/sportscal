const existingFeed = normalizeFeed(readJson(INPUT_PATH));
const existingCwgIds = new Set(existingFeed.events.filter(event => event.key === "cwg").map(event => event.id));
const nextFeed = normalizeFeed({
  ...existingFeed,
  version: FEED_VERSION,
  publishedAt: REVIEWED_AT,
  sourceNote: "Source-backed historical results plus curated, spoiler-safe Glasgow 2026 Commonwealth Games sessions.",
  events: [
    ...existingFeed.events.filter(event => event.key !== "cwg"),
    ...cards,
  ].sort((first, second) => `${first.date}T${first.time}${first.id}`.localeCompare(`${second.date}T${second.time}${second.id}`)),
});

const errors = validateFeed(nextFeed);
if (errors.length) {
  console.error("Refusing to write invalid Glasgow 2026 cards:");
  errors.forEach(error => console.error(`- ${error}`));
  process.exit(1);
}

writeJson(INPUT_PATH, nextFeed);
fs.mkdirSync(path.dirname(BUNDLE_PATH), { recursive: true });
fs.writeFileSync(
  BUNDLE_PATH,
  `(function attachBundledCommonwealthGames(root){\n  root.NOTHINGSPORTS_CWG_EVENTS = Object.freeze(${JSON.stringify(cards, null, 2)}.map(event => Object.freeze(event)));\n})(typeof globalThis !== "undefined" ? globalThis : window);\n`,
);

console.log(`Upserted ${cards.length} Glasgow 2026 Commonwealth Games cards into ${INPUT_PATH}`);
console.log(`Replaced ${existingCwgIds.size} earlier Commonwealth Games cards`);
console.log(`Updated direct-file fallback bundle ${BUNDLE_PATH}`);
